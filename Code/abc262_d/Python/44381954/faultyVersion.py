MOD = 998244353
# MOD = 10 ** 9 + 7
# DIR = ((-1, 0), (0, 1), (1, 0), (0, -1))

def solve() -> None:
    n = sint()
    nums = ints()
    ans = 0
    for i in range(1, n + 1):   # 选择i个数
        dp = [[0] * n for _ in range(n + 1)]
        dp[0][0] = 1
        for x in nums:  # 枚举所有数字
            for j in range(i, 0, -1):   # 前面选择了j个数
                for k in range(i):  # 模i余为k
                    dp[j][(k + x) % i] = (dp[j][(k + x) % i] + dp[j - 1][k]) % MOD
        ans += dp[i][0]
    print(ans)

solve()